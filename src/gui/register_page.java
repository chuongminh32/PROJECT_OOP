    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String email = jTextField1.getText();
        String password = new String(jPasswordField2.getPassword());
        String confirm_password = new String(jPasswordField1.getPassword());

        boolean registerSuccess = AuthController.register(email, password, confirm_password);
        handleRegisterSuccess(registerSuccess);
    }

    private void handleRegisterSuccess(boolean registerSuccess) {
        if (registerSuccess) {
            JOptionPane.showMessageDialog(null, "Đăng kí thành công! chuyển sang trang đăng nhập", "Thành công", JOptionPane.INFORMATION_MESSAGE);
            login_page login = new login_page();
            login.setVisible(true);
            login.pack();
            login.setLocationRelativeTo(null); 
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Đăng kí thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

